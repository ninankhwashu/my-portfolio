document.addEventListener("DOMContentLoaded", function () {
  const testimonialCards = Array.from(
    document.querySelectorAll(".testimonial-card")
  );
  const testimonialPopup = document.querySelector(".testimonial-popup");
  const popupImg = document.querySelector(".testimonial-popup-img");
  const closeBtn = document.querySelector(".testimonial-close");
  const prevBtn = document.querySelector(".testimonial-prev");
  const nextBtn = document.querySelector(".testimonial-next");

  let currentIndex = 0;
  const testimonialImages = testimonialCards.map(
    (card) => card.querySelector("img").src
  );

  testimonialCards.forEach((card, index) => {
    card.addEventListener("click", function () {
      currentIndex = index;
      updatePopupImage();
      testimonialPopup.classList.add("active");
      document.body.style.overflow = "hidden";
    });
  });

  function updatePopupImage() {
    popupImg.src = testimonialImages[currentIndex];
  }

  function showNext() {
    currentIndex = (currentIndex + 1) % testimonialImages.length;
    updatePopupImage();
  }

  function showPrev() {
    currentIndex =
      (currentIndex - 1 + testimonialImages.length) % testimonialImages.length;
    updatePopupImage();
  }

  nextBtn.addEventListener("click", function (e) {
    e.stopPropagation();
    showNext();
  });

  prevBtn.addEventListener("click", function (e) {
    e.stopPropagation();
    showPrev();
  });

  document.addEventListener("keydown", function (e) {
    if (!testimonialPopup.classList.contains("active")) return;

    if (e.key === "ArrowRight") {
      showNext();
    } else if (e.key === "ArrowLeft") {
      showPrev();
    } else if (e.key === "Escape") {
      closePopup();
    }
  });

  function closePopup() {
    testimonialPopup.classList.remove("active");
    document.body.style.overflow = "";
  }

  closeBtn.addEventListener("click", closePopup);

  testimonialPopup.addEventListener("click", function (e) {
    if (e.target === this) {
      closePopup();
    }
  });
});
